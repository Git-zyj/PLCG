#pragma scop
for (int i = 0; i < PB_N-1; i++) {
    for (int j = 0; j < PB_Q; j++) {
        for (int k = 1; k < PB_L-2; k++) {
            A[k+1][k][j] = A[k+1][k-1][j] - A[k+2][k][j] + A[k][j][i] + 6;
        }
    }
    B[i] = B[i] * B[i] + C[i] + C[i+1] - 4;
}
for (int l = 1; l < PB_M-1; l++) {
    for (int m = 2; m < l; m++) {
        D[l][m] = D[l-1][m] * D[l][m-2] + E[l+1][l] * 3;
    }
    for (int n = 1; n < PB_M-1; n++) {
        D[n+1][n+1] = D[n-1][n+1] - D[n+1][n+1] * E[n][l+1] - D[l+1][n] - 4;
        for (int o = 0; o < l; o++) {
            F[l][n+1][l] = D[n+2][n+1] + A[l][n][o] + 5;
        }
    }
}
for (int p = 0; p < PB_L; p++) {
    B[p] = B[p] - B[p] + 1;
}
#pragma endscop
