void kernel_3614121222_00(int xa,int ya,int za,DATA_TYPE POLYBENCH_3D(A,xA,yA,zA,xa,ya,za),int xb,int yb,DATA_TYPE POLYBENCH_2D(B,xB,yB,xb,yb)){
polybench_start_instruments;
#pragma scop
for (int i = 1; i < PB_L-1; i++) {
    for (int j = 2; j < PB_L; j++) {
        for (int k = 0; k < PB_M; k++) {
            A[j][i-1][j] = B[i][j] + B[i][j-1] + 1;
        }
        for (int l = 0; l < PB_M-1; l++) {
            A[l][j][l] = A[l][j-2][l+1] * A[l+1][i][l] - A[l][j][i] - 1;
        }
    }
    for (int m = 0; m < PB_L; m++) {
        for (int n = 0; n < PB_L; n++) {
            A[m][i+1][n] = A[i][m][n+1] + B[n][m] - 5;
        }
    }
}
for (int o = 1; o < PB_L; o++) {
    for (int p = 1; p < o; p++) {
        for (int q = 0; q < PB_M; q++) {
            B[p][o+1] = B[p][o+1] - A[q][o][p] + A[o][o-1][p] + 1;
        }
        for (int r = 0; r < PB_P; r++) {
            A[o][p][r] = A[o][p-1][r] * A[o][p][r] - 6;
        }
    }
    for (int s = 0; s < PB_L; s++) {
        B[s][o] = 6;
    }
}
#pragma endscop
polybench_stop_instruments;
polybench_print_instruments;
}
